class Solution {
  public:
    void sort012(vector<int>& arr) {
        int m[]={0,0,0};
        for(int i:arr) m[i]++;
        for(int& i:arr) {
            if(m[0]) {
                i=0;
                m[0]--;
            }
            else if(m[1]) {
                i=1;
                m[1]--;
            }
            else i=2;
        }
    }
};

