class Solution {
  public:

    string longestCommonPrefix(vector<string> arr) {
        string x=arr[0];
        int n=x.size();
        for(string& word:arr) for(int i=0;i<n;i++) if(x[i]!=word[i]) x=x.substr(0,i);
        return x.empty()?"-1":x;
    }
};

